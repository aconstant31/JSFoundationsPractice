let day = "Tuesday"

switch (day) {
    case "Monday":
        console.log("Bummer.");
        break;
    case "Tuesday":
        console.log("Here we go again!");
        break;
    case "Wednesday":
        console.log("Stop.");
        break;
    case "Thursday":
        console.log("Why?");
        break;
    case "Friday":
        console.log("So close!");
        break;
    case "Saturday":
        console.log("WEEKEND!!");
        break;
    case "Sunday":
        console.log("Oh no almost over...");
        break;
    default:
        console.log("Not a weekday.");
        break;
}

